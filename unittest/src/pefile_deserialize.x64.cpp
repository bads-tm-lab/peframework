void PEFILE_DESERIALIZE_X64_TESTS( void )
{
    printf( "testing PEFile X64 deserialize 'write.exe'..." );
    {
        PEFile image;
        {
            PEStreamConstMem memStream( _write_exe_sample, sizeof(_write_exe_sample) );

            image.LoadFromDisk( &memStream );
        }

        // TODO: check properties.
        assert( image.IsDynamicLinkLibrary() == false );
    }
    printf( "ok.\n" );

    printf( "testing PEFile X64 semi-idempotence processing 'write.exe'..." );
    {
        PEFile image;

        // First load the image.
        {
            PEStreamConstMem memStream( _write_exe_sample, sizeof(_write_exe_sample) );

            image.LoadFromDisk( &memStream );
        }

        // Next write the image to another buffer.
        PEStreamDynMem outStream;

        image.WriteToStream( &outStream );

        // Check that it got written properly.
        assert( outStream.Size() == sizeof(_write_exe_sample) );

        // We do not compare for equal memory because the Windows PE file writer is a historical
        // application that writes certain optimized-for-itself constructs that do not pass the
        // test-of-time.
        // But this also means that we do NOT have a _true/complete_ idempotence test.
    }
    printf( "ok.\n" );
}